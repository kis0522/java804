package sec01.exam01;

public class VariableInitializationExample {
	//VariableInitializationExample = 변수 초기화 예
	public static void main(String[] args) {
		// 변수 value 선언하고 초기화 정수 10을 대입
		int value = 20;
		// 변수 value의 값을 읽어서 10을 더하는 연산을 수행한 다음 연산의 결과값을 변수 reuslt의 초기값으로 저장
		int reuslt = value + 10;
		System.out.println(reuslt);
	}
}